/*
 * This file contains an minimal example of a Linear "OpMode". An OpMode is a 'program' that runs in either
 * the autonomous or the teleop period of an FTC match. The names of OpModes appear on the menu
 * of the FTC Driver Station. When a selection is made from the menu, the corresponding OpMode
 * class is instantiated on the Robot Controller and executed.
 *
 * This particular OpMode just executes a basic Tank Drive Teleop for a two wheeled robot
 * It includes all the skeletal structure that all linear OpModes contain.
 *
 * Use Android Studio to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this OpMode to the Driver Station OpMode list
 */
@Deprecated
@Disabled
@TeleOp(name="Test Spline", group="Test")
public class OLD_Test_Spline extends LinearOpMode {

    // Declare OpMode members.
    private final ElapsedTime runtime = new ElapsedTime();

    @Override
    public void runOpMode() {
        telemetry.addData("Status", "Initialized");
        telemetry.update();

        SampleMecanumDrive drive = new SampleMecanumDrive(hardwareMap);

        // Wait for the game to start (driver presses START)
        waitForStart();
        runtime.reset();

        drive.setPoseEstimate(new Pose2d(-36.04, -60.34, Math.toRadians(90.00)));

        TrajectorySequence trajectory0 = drive.trajectorySequenceBuilder(new Pose2d(-36.04, -60.34, Math.toRadians(90.00)))
            .splineTo(new Vector2d(-49.99, 3.33), Math.toRadians(89.19))
            .splineTo(new Vector2d(-35.25, 38.15), Math.toRadians(89.54))
            .splineTo(new Vector2d(44.29, 47.97), Math.toRadians(15.02))
            .splineTo(new Vector2d(31.31, -53.50), Math.toRadians(246.80))
            .splineTo(new Vector2d(-35.96, -60.77), Math.toRadians(185.32))
            .build();

        if(isStopRequested()) return;

        drive.followTrajectorySequence(trajectory0);
    }
}
